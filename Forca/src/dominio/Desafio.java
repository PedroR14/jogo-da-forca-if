package dominio;

public class Desafio {
	
	private int id_usuario_remetente;
	private int id_usuario_destinatario;
	private int id_forca;
	private int aposta;
	
	public int getId_usuario_remetente() {
		return id_usuario_remetente;
	}
	public void setId_usuario_remetente(int id_usuario_remetente) {
		this.id_usuario_remetente = id_usuario_remetente;
	}
	public int getId_usuario_destinatario() {
		return id_usuario_destinatario;
	}
	public void setId_usuario_destinatario(int id_usuario_destinatario) {
		this.id_usuario_destinatario = id_usuario_destinatario;
	}
	public int getId_forca() {
		return id_forca;
	}
	public void setId_forca(int id_forca) {
		this.id_forca = id_forca;
	}
	public int getAposta() {
		return aposta;
	}
	public void setAposta(int aposta) {
		this.aposta = aposta;
	}
	

}
